-- 7. Create System Notifications table
create table if not exists public.notifications (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users(id) on delete cascade, -- NULL means global notifications
  title text not null,
  message text not null,
  type text not null, -- 'system', 'achievement', 'reminder', etc.
  link text,
  created_at timestamptz default now() not null
);

alter table public.notifications enable row level security;

create policy "Users can view relevant notifications"
  on public.notifications for select
  using (user_id is null or user_id = auth.uid());

create policy "Allow all users to insert notifications"
  on public.notifications for insert
  with check (true);

create policy "Allow all users to delete notifications"
  on public.notifications for delete
  using (true);


-- 8. Create Read Notifications junction table (for read states)
create table if not exists public.read_notifications (
  user_id uuid references auth.users(id) on delete cascade not null,
  notification_id bigint references public.notifications(id) on delete cascade not null,
  read_at timestamptz default now() not null,
  primary key (user_id, notification_id)
);

alter table public.read_notifications enable row level security;

create policy "Users can read own read states"
  on public.read_notifications for select
  using (auth.uid() = user_id);

create policy "Users can mark notifications as read"
  on public.read_notifications for insert
  with check (auth.uid() = user_id);
